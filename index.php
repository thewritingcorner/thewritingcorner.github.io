            <em>Writing in the Secondary School: A Guide for Teachers and Pre-service Teachers</em> was developed to help practitioners in the teaching profession foster and improve, through awareness and practical approaches, their skills in teaching writing to promote student adeptness or proficiency in English. 
            It is designed particularly for teachers to recognize that the process of writing is not a linear one. Therefore, the focus is on teaching writing using methods of engagement to encourage student involvement in writing, individually and collaboratively, so that when they communicate in writing, it is effective. 
            It is, also, designed so that teachers can make learning and writing fun and interactive, to generate awareness of the difficulties that exist in student writing and develop instructional designs to correct them accordingly.
